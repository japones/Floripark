    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        String login = txtUsuario.getText();
        String senha = new String(pfSenha.getPassword());
        login = login.toUpperCase();
        Usuario usuarioLogado = null;
        try {
            usuarioLogado = ServiceFactory.getUsuarioService().isUsuarioValido(login, senha);
        } catch (Exception ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (usuarioLogado != null) {
            Principal tp = new Principal(usuarioLogado);
            tp.setLocationRelativeTo(null);
            tp.setVisible(true);
            tp.setExtendedState(MAXIMIZED_BOTH);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Usu치rio ou senha inv치lido!");
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

